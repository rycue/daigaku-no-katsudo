public class Day03_RickyCuenza {
    public static void main(String[] args) {

        int num1 = 11;
        int num2 = 22;
        double num3 = 33.33;
        double num4 = 44.44;
        int result1 = 0;
        double result2 = 0;

        result1 = num1 + num2;
        System.out.println(num1 + " + " + num2 + " = " + result1);

        result1 = num1 - num2;
        System.out.println(num1 + " - " + num2 + " = " + result1);

        result1 = num1 % num2;
        System.out.println("The remainder of " + num1 + " % " + num2 + " is " + result1);
                
        result2 = num3 * num4;
        System.out.println(num3 + " * " + num4 + " = " + result2);

        result2 = num3 / num4;
        System.out.println(num3 + " / " + num4 + " = " + result2);

        System.out.println("\nProgram by Ricky Cuenza (JOYSIS TVI - Batch 3)");
    }
}
